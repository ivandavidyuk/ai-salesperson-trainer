// Поле prompt — роль, которую играет ИИ. Backend склеивает его с промптом
// выбранного типа тренировки и отдаёт LLM системным сообщением.
//
// Промпты живут здесь, а не в БД: источник правды — репозиторий, изменения
// видны в истории git. Поэтому скрипт ПЕРЕЗАПИСЫВАЕТ промпты при каждом
// запуске — правки, сделанные напрямую в базе, будут потеряны.
//
// У Тамары Михайловны промпт настоящий (перенесён из backend как есть),
// у остальных — временные заглушки в одно предложение.
//
// Скрипт идемпотентный: пациентов ищет по имени и обновляет.

